package Assignments;

public class AirthematicOperators {

	public static void main(String[] args) {
		
		int a = 10;
		int b = 20;
		int c = 30;
		int d = 40;
		
		int e = a+b+c+d;
		int f = a*b*c*d;
		int g = a/b/c/e;
		int h = a-b-c-d;
		
		System.out.println("Addition of numbers is "+ e);
		System.out.println("Multiplication of numbers is "+ f);
		System.out.println("Division of numbers is "+ g);
		System.out.println("Substraction of numbers is "+ h);

	}

}
